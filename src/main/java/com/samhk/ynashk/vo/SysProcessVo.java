package com.samhk.ynashk.vo;

import java.util.List;

public class SysProcessVo extends BaseVo{
    private String processCode;
    private String processChiName;
    private String processEngName;
    private String processRemark;
    private List<ProcessPrivilegeVo> processPrivilegeList;


    public List<ProcessPrivilegeVo> getProcessPrivilegeList() {
        return processPrivilegeList;
    }
    public void setProcessPrivilegeList(
            List<ProcessPrivilegeVo> processPrivilegeList) {
        this.processPrivilegeList = processPrivilegeList;
    }
    public String getProcessCode() {
        return processCode;
    }
    public void setProcessCode(String processCode) {
        this.processCode = processCode;
    }
    public String getProcessChiName() {
        return processChiName;
    }
    public void setProcessChiName(String processChiName) {
        this.processChiName = processChiName;
    }
    public String getProcessEngName() {
        return processEngName;
    }
    public void setProcessEngName(String processEngName) {
        this.processEngName = processEngName;
    }
    public String getProcessRemark() {
        return processRemark;
    }
    public void setProcessRemark(String processRemark) {
        this.processRemark = processRemark;
    }
}
